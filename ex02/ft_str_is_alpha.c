int	ft_str_is_alpha(char *str)
{
	int	i;

	i = 0;
	while ((str[i] >= 97 && str[i] <= 122) || (str[i] >= 65 && str[i] <= 90))
		i += 1;
	if (str[i] != '\0')
		return (0);
	else
		return (1);
}
